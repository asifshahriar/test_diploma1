package com.example.udiploma.Model;

public class Notice {
    private String title, uri, time, date;

    public Notice() {

    }

    public Notice(String title, String uri, String time, String date) {
        this.title = title;
        this.uri = uri;
        this.time = time;
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
